n1 = int(input('digite um numero: '))
n2 = int(input('digite outro numero: '))
m = n1 * n2
s = n1 + n2
d = n1 / n2 
di = n1 // n2
e = n1 ** n2
print('A multiplicacao entre \n {} e {} vale {}'.format(n1, n2, m), end=' ')
print('A soma entre {} e {} vale {}'.format(n1, n2, s))
print('A divisao entre {} e {} vale {}'.format(n1, n2, d))
print('A divisao inteira entre {} e {} vale {}'.format(n1, n2, di))
print('A exponenciacao entre {} e {} vale {}'.format(n1, n2, e))